#include "libft.h"

void	*ft_memset(void *s, int c, size_t len)
{
	size_t	i;

	i = 0;
	if (len <= 0)
		return (0);
	while (i < len)
	{
		((char *)s)[i] = (unsigned char) c;
		i++;
	}
	return (s);
}